package com.kami.entity;


public class User {

    private String userName;

    private Integer sex;

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setSex(Integer sex) {
        this.sex = sex;
    }

    public String getUserName() {
        return userName;
    }

    public Integer getSex() {
        return sex;
    }

}
